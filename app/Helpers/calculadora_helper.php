{
    $c= $a+$b;
    return $c;
}
function restar($a, $b)
{
    $c= $a-$b;
    return $c;
}
function multiplicar($a, $b)
{
    $c= $a*$b;
    return $c;
}
function dividir($a, $b)
{
    $c= $a*$b;
    return $c;
}

function recArea($a, $b)
{
    $c= $a*$b;
    return $c;
}

function recPerimetro($a, $b)
{
    $c= $a*2+$b*2;
    return $c;
}
